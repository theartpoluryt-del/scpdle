function createChallengeHtml(scp, index, progress) {
  const data = getChallengeData(scp);
  const mistakes = progress.mistakes;

  if (index === 0) {
    return `
      <div class="classic-panel">
        <p>Вводи SCP и сравнивай признаки. Зеленый - совпадение, желтый - частично похоже, серый - мимо.</p>
        ${createClassicTableHtml(scp, progress.guesses ?? [])}
      </div>
    `;
  }

  if (index === 1) {
    return `
      <div class="redacted-file">
        <span class="stamp">Foundation File</span>
        <p>${progress.revealed ? buildDocumentExcerpt(scp, data) : redactText(buildDocumentExcerpt(scp, data), mistakes)}</p>
      </div>
    `;
  }

  if (index === 2) {
    const visibleEmoji = data.emoji.slice(0, Math.min(1 + mistakes, data.emoji.length));
    return `
      <div class="emoji-panel">
        <div class="emoji-line">${visibleEmoji.map((emoji) => `<span>${emoji}</span>`).join("")}</div>
        <p>${mistakes === 0 ? "Один символ. Попробуй поймать главную ассоциацию." : "После ошибки добавлен новый символ."}</p>
      </div>
    `;
  }

  if (index === 3) {
    const classificationFields = buildClassificationFields(data);
    const visibleFields = classificationFields.slice(0, Math.min(mistakes + 1, classificationFields.length));

    return `
      <div class="classification-panel">
        <span class="stamp">Foundation Index Card</span>
        <p>Восстанови объект по служебной классификации. После каждой ошибки открывается одно новое поле: от общих признаков к частным.</p>
        <ol class="classification-list">
          ${visibleFields.map((field) => `
            <li class="visible">
              <span>${field.label}</span>
              <strong>${field.value}</strong>
            </li>
          `).join("")}
        </ol>
      </div>
    `;
  }

  const drawingStyle = getDrawingRevealStyle(progress);
  const drawingHint = redactDrawingHint(data.drawingHint, progress);

  return `
    <div class="drawing-panel">
      <div class="mystery-portrait drawing-reveal" style="${drawingStyle}">${createPaintSketchHtml(scp, progress)}</div>
      <p class="drawing-hint">${drawingHint}</p>
    </div>
  `;
}

