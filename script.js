function render() {
  const streakUpdated = updateStreakIfNeeded();
  const dayState = state.days[today] ?? {};
  const solved = daily.filter((scp) => dayState[scp.id]?.solved).length;
  const activeIndex = getSelectedIndex(dayState);
  const completed = daily.filter((scp) => dayState[scp.id]?.solved).length;

  streakEl.textContent = state.streak;
  solvedTodayEl.textContent = `${completed}/5`;
  renderProgress(activeIndex, dayState);
  renderYesterdayList();
  renderDevAnswers();

  gameEl.classList.toggle("is-static-render", !allowEntryAnimations);
  gameEl.innerHTML = "";
  if (completed > 0) {
    gameEl.appendChild(createSolvedList(dayState));
  }

  const scp = daily[activeIndex];
  const progress = dayState[scp.id] ?? { mistakes: 0, solved: false };

  if (completed === daily.length) {
    gameEl.appendChild(createCard(scp, activeIndex, progress));
    resultPanel.hidden = false;
    resultText.textContent = `Отлично: все 5 досье закрыты. Текущий streak: ${state.streak}.`;
    submitTodayScore();
  } else {
    gameEl.appendChild(createCard(scp, activeIndex, progress));
    resultPanel.hidden = true;
  }

  if (streakUpdated) saveState();
  if (!allowEntryAnimations) {
    requestAnimationFrame(() => {
      allowEntryAnimations = true;
      gameEl.classList.remove("is-static-render");
    });
  }
}

function getSelectedIndex(dayState) {
  const storedIndex = Number(dayState.__selectedIndex ?? 0);
  if (Number.isInteger(storedIndex) && storedIndex >= 0 && storedIndex < daily.length) {
    return storedIndex;
  }
  return 0;
}

function renderProgress(activeIndex, dayState) {
  progressEl.innerHTML = "";
  daily.forEach((_, index) => {
    const mode = levelTypes[index];
    const progress = dayState[daily[index].id];
    const step = document.createElement("button");
    step.type = "button";
    step.disabled = false;
    step.className = "progress-step";
    if (progress?.solved) step.classList.add("done");
    if (progress?.revealed && !progress?.solved) step.classList.add("revealed");
    if (index === activeIndex && activeIndex < daily.length) step.classList.add("current");
    step.innerHTML = `
      <strong>${mode.name}</strong>
      <span>${mode.subtitle}</span>
    `;
    step.addEventListener("click", () => selectLevel(index));
    progressEl.appendChild(step);
  });
}

