function checkAnswer(scp, index, value, message) {
  const answer = normalize(value);
  if (!answer) {
    message.textContent = "Введи номер или название объекта.";
    return;
  }

  if (!state.days[today]) state.days[today] = {};
  if (!state.days[today][scp.id]) state.days[today][scp.id] = { mistakes: 0, solved: false, guesses: [] };

  const progress = state.days[today][scp.id];
  const guessedScp = findScpByAnswer(answer);
  if (!guessedScp) {
    message.textContent = "Такого SCP пока нет в базе. Выбери вариант из подсказок.";
    return;
  }

  const correct = guessedScp.id === scp.id;

  if (correct) {
    progress.revealed = true;
    progress.revealedAt = new Date().toISOString();
    if (!progress.guesses.includes(guessedScp.id)) {
      progress.guesses.push(guessedScp.id);
    }
  } else {
    progress.mistakes += 1;
    if (!progress.guesses.includes(guessedScp.id)) {
      progress.guesses.push(guessedScp.id);
    }
    message.textContent = index === 0
      ? "Не то. Сравнение добавлено в таблицу."
      : "Не то. Открыта новая подсказка.";
  }

  render();
}

function completeLevel(scp) {
  if (!state.days[today]) state.days[today] = {};
  if (!state.days[today][scp.id]) state.days[today][scp.id] = { mistakes: 0, solved: false, guesses: [] };

  const progress = state.days[today][scp.id];
  progress.revealed = true;
  progress.solved = true;
  progress.solvedAt = new Date().toISOString();
  const currentIndex = daily.findIndex((item) => item.id === scp.id);
  const nextIndex = getNextUnsolvedIndex(currentIndex);
  state.days[today].__selectedIndex = nextIndex === -1 ? currentIndex : nextIndex;
  saveState();
  render();
}

function getNextUnsolvedIndex(currentIndex) {
  if (!state.days[today]) return 0;
  for (let offset = 1; offset <= daily.length; offset += 1) {
    const index = (currentIndex + offset) % daily.length;
    if (!state.days[today][daily[index].id]?.solved) return index;
  }
  return -1;
}

function pickDailyScps(dateKey) {
  const previousIds = new Set(pickDailyScpsRaw(addDaysToDateKey(dateKey, -1)).map((scp) => scp.id));
  return pickDailyScpsRaw(dateKey, previousIds);
}

function pickDailyScpsRaw(dateKey, excludedIds = new Set()) {
  const shuffled = [...scps].sort((a, b) => seededScore(dateKey + a.id) - seededScore(dateKey + b.id));
  return shuffled.filter((scp) => !excludedIds.has(scp.id)).slice(0, 5);
}

function seededScore(text) {
  let hash = 2166136261;
  for (let i = 0; i < text.length; i += 1) {
    hash ^= text.charCodeAt(i);
    hash = Math.imul(hash, 16777619);
  }
  return hash >>> 0;
}

function searchTokens(scp) {
  return [scp.id, scp.title, ...scp.aliases].map(normalize);
}

function findScpByAnswer(answer) {
  return scps.find((scp) => searchTokens(scp).includes(answer));
}

function normalize(value) {
  return value
    .toLowerCase()
    .replaceAll("ё", "е")
    .replace(/[^a-zа-я0-9]/g, "");
}

function getMoscowDateKey(date = new Date()) {
  const parts = new Intl.DateTimeFormat("ru-RU", {
    timeZone: "Europe/Moscow",
    year: "numeric",
    month: "2-digit",
    day: "2-digit"
  }).formatToParts(date);
  const byType = Object.fromEntries(parts.map((part) => [part.type, part.value]));
  const year = byType.year;
  const month = byType.month;
  const day = byType.day;
  return `${year}-${month}-${day}`;
}

function getActiveDateKey() {
  const offset = state.devDayOffset ?? 0;
  return addDaysToDateKey(getMoscowDateKey(), offset);
}

function addDaysToDateKey(dateKey, days) {
  const [year, month, day] = dateKey.split("-").map(Number);
  const date = new Date(Date.UTC(year, month - 1, day));
  date.setUTCDate(date.getUTCDate() + days);
  return date.toISOString().slice(0, 10);
}

function updateActiveDay() {
  today = getActiveDateKey();
  yesterdayDaily = pickDailyScpsRaw(addDaysToDateKey(today, -1));
  daily = pickDailyScps(today);
}

