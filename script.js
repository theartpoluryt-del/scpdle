function getDrawingRevealStyle(progress) {
  const maxMistakes = 10;
  const mistakes = Math.min(progress.mistakes ?? 0, maxMistakes);
  const reveal = progress.revealed ? 1 : mistakes / maxMistakes;
  const scale = Math.round((1.08 - reveal * 0.08) * 1000) / 1000;
  return `--drawing-scale: ${scale};`;
}

function redactDrawingHint(text, progress) {
  const mistakes = progress.mistakes ?? 0;
  if (progress.revealed || mistakes >= 10) return text;
  const revealEvery = mistakes >= 5 ? 3 : 0;
  return text
    .split(" ")
    .map((word, index) => revealEvery > 0 && index % revealEvery === 0 ? word : createRedaction(word))
    .join(" ");
}

function createRedaction(word) {
  const length = Math.min(12, Math.max(4, word.length));
  return `<span class="redaction">${"\u2588".repeat(length)}</span>`;
}

